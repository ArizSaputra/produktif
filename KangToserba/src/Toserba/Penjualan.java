    private void btn_tambahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_tambahMouseClicked
        try {
            Integer.parseInt(txt_jumlah.getText());
            row = tb_barang.getSelectedRow();
            String idkolom = tb_barang.getValueAt(row, 0).toString(); 
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_sinarharapanpos", "root", "");
            String query = "SELECT kuantitas FROM tb_barang WHERE id_barang= '"+idkolom+"'" ;
            PreparedStatement ps = con.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            if(rs.next()) {
                if (txt_jumlah.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "Masukkan jumlah barang");
                } else {
                    if(Integer.valueOf(rs.getString("kuantitas")) >= Integer.valueOf(txt_jumlah.getText())) {
                        result=idlist.contains(tb_barang.getValueAt(row,0).toString());
                         if(result==false){
                            idlist.add(tb_barang.getValueAt(row,0).toString());
                            String data[] = {tb_barang.getValueAt(row, 0).toString(), tb_barang.getValueAt(row, 1).toString(), tb_barang.getValueAt(row, 2).toString(), txt_jumlah.getText()};
                            modelpj.addRow(data);
                            _setsubtotal();
                         } else{
                            JOptionPane.showMessageDialog(null, "Data barang sudah terinput");
                        }
                    } else{   
                            JOptionPane.showMessageDialog(null, "Stok barang kurang");
                    }
                    
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Masukkan jumlah dengan benar");
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btn_tambahMouseClicked

