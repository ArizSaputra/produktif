        public void generate() {
        String query = "SELECT * FROM tb_penjualan join tb_detail_penjualan right join tb_barang on tb_barang.id_barang=tb_detail_penjualan.id_barang";
        String path = "C:\\Users\\Ariz\\Downloads\\KangToserba\\src\\Report\\report1.jrxml";

        try {
              Connection koneksi = (Connection) Config.configDB();
            java.sql.Statement pstCek = koneksi.createStatement();
            ResultSet res = pstCek.executeQuery(query);
            JasperDesign design = JRXmlLoader.load(path);
            JasperReport jr = JasperCompileManager.compileReport(design);
            JRResultSetDataSource rsDataSource = new JRResultSetDataSource(res);
            JasperPrint jp = JasperFillManager.fillReport(jr, new HashMap<>(), rsDataSource);

            JasperViewer.viewReport(jp);
        } catch(Exception e) { e.printStackTrace(); }
    }
