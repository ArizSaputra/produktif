    private void btn_tambah2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_tambah2MouseClicked
        // TODO add your handling code here:
        try{
            Integer.parseInt(txt_jumlah.getText());

            int row = tb_barang.getSelectedRow();
            boolean result=idlist.contains(tb_barang.getValueAt(row,0).toString());
            if(result==false){
                idlist.add(tb_barang.getValueAt(row,0).toString());
                String data[] = {tb_barang.getValueAt(row, 0).toString(), tb_barang.getValueAt(row, 1).toString(), tb_barang.getValueAt(row, 2).toString(), txt_jumlah.getText()};
                modelpj.addRow(data);
            } else {
                JOptionPane.showMessageDialog(this, "Data barang sudah terinput");
            }
            _setsubtotal();
        }

        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Masukkan jumlah dengan benar");
        }
    }//GEN-LAST:event_btn_tambah2MouseClicked
    private void _setsubtotal() {
        int row = tb_penjualan.getRowCount();
        int subtotal = 0;
        for (int i = 0; i < row; i++) {
            int harga_barang = Integer.parseInt(tb_penjualan.getValueAt(i, 2).toString()); //22000
            int jumlah = Integer.parseInt(tb_penjualan.getValueAt(i, 3).toString()); //11
            subtotal = subtotal + (harga_barang * jumlah);
          //subtotal = 15000 + (23*11)
          //subtotal = 15000 + 253
          //subtotal = 15253
          //subtotal = 15253 + (22000*11)
          //subtotal = 15253 + 242000
          //subtotal = 257253
        }
        this.sub_total = subtotal;
        txt_subtotal.setText(String.valueOf(this.sub_total));
        _settotal();
    }
    private void _setjasakirim(){
        this.berat = Integer.valueOf(txt_berat.getText());
        this.jarak = Integer.valueOf(txt_jarak.getText());
        this.ongkir = berat * jarak * 3000;
        txt_jasakirim.setText(String.valueOf(this.ongkir));
        _settotal();
     
    }
    
    public void _settotal(){
        this.total = this.sub_total + this.ongkir;
        txt_total.setText(String.valueOf(this.total));
    }
    public void reset(){
        this.sub_total = 0;
        this.total = 0;
        txt_subtotal.setText("0");
        txt_total.setText("0");
        txt_jumlah.setText("0");
        txt_jasakirim.setText("0");
        txt_berat.setText("0");
        txt_jarak.setText("0");
        modelpj.setRowCount(0);
    }
