    private void btn_bayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_bayarActionPerformed
       int rows = tb_penjualan.getRowCount();
       String idkaryawan = Login.idkaryawan;
       String query;
       PreparedStatement ps;
       
       try{
          Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_sinarharapanpos","root","");
          query = "INSERT INTO tb_penjualan VALUES (null,now(),'"+this.total+"','"+idkaryawan+"')";
           try {
               con.setAutoCommit(false);
               ps = con.prepareStatement(query,Statement.RETURN_GENERATED_KEYS);     
               ps.executeUpdate();
               ResultSet rs = ps.getGeneratedKeys();
//               System.out.println("rows : "+rows);
                if(rs.next()){
                    for (int i = 0; i < rows; i++) {
                        query = "INSERT INTO tb_detail_penjualan VALUES ('"+rs.getInt(1)+"','"+tb_penjualan.getValueAt(i, 0).toString()+"','"+tb_penjualan.getValueAt(i, 3).toString()+"')";
                        ps = con.prepareStatement(query);
                        ps.execute();
                        System.out.println(Integer.parseInt(tb_penjualan.getValueAt(i, 3).toString()));
                    }   
                }
               if(opt_jasakirim.isSelected()){
                   query = "INSERT INTO tb_ongkir VALUES (null,'"+rs.getInt(1)+"','"+this.berat+"','"+this.jarak+"','"+this.ongkir+"')";
                    ps = con.prepareStatement(query);
                    ps.execute();
               } 
                String uang = JOptionPane.showInputDialog("Bayar : ");
                if(Integer.valueOf(uang)>=this.total){
                    con.commit();
                    JOptionPane.showMessageDialog(this,"Kembalian : " + (Integer.valueOf(uang)- this.total));
                    idlist.clear();
                    generate();
                    reset();
                    
                } else {
//                result = false;
                JOptionPane.showMessageDialog(null,"Uang yang dibayarkan tidak cukup");
               con.close();
                } 
                   System.out.println(uang);
                
           } catch (Exception e) {
               con.rollback();
               System.out.println(e.getMessage());
           }
       } 
       catch(Exception e){
       }
    }//GEN-LAST:event_btn_bayarActionPerformed

