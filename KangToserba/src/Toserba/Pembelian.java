    public void tampilkanbarang(){
        DefaultTableModel model;
//        String[] judul = {"ID", "Nama Barang", "Harga"};
        String[] judul = {"ID","Nama Barang", "Harga","Stok"};
        model = new DefaultTableModel(judul, 0);
        tb_barang.setModel(model);
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_sinarharapanpos", "root", "");
            ResultSet rs = con.createStatement().executeQuery("SELECT * FROM tb_barang ");
            while (rs.next()) {
//                String data[] = {rs.getString(2), rs.getString(5),rs.getString(3)};
                String data[] = {rs.getString(1), rs.getString(2), rs.getString(5), rs.getString(3)};
                model.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(dataBarang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
