    private void _setsubtotal() {
        int row = tb_pembelian.getRowCount();
        int subtotal = 0;
        for (int i = 0; i < row; i++) {
            int harga_barang = Integer.parseInt(tb_pembelian.getValueAt(i, 2).toString());
            int jumlah = Integer.parseInt(tb_pembelian.getValueAt(i, 3).toString());
            subtotal = subtotal + (harga_barang * jumlah);
        }
        this.sub_total = subtotal;
        txt_subtotal.setText(String.valueOf(this.sub_total));
        _settotal();
    }
